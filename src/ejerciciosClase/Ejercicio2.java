public class Ejercicio2 {
    public static void main(String[] args) {
        int X = 144;
        int Y = 999;
        double N = 3.5;
        double M = 8.9;
        //X e Y
        //vista de valores
        System.out.println("El valor de X es: " + X+"\n" + "El valor de Y es: " + Y + "." + "\n");
        //Operaciones
        System.out.println("La suma de X + Y es: " + (X + Y) + "." + "\n");
        System.out.println("La resta de X - Y es: " + (X - Y) + "." + "\n");
        System.out.println("La multiplicacion de X * Y es: " + (X * Y) + "." + "\n");
        System.out.println("La division de X / Y es: " + (X / Y) + "." + "\n");
        System.out.println("El resto de X % Y es: " + (X % Y) + "." + "\n");
        //N y M
        //vista de valores 
        System.out.println("El valor de N es: " + N+"\n" + "El valor de M es: " + M + "." + "\n");
        //Operaciones
        System.out.println("La suma de N + M es: " + (N + M) + "." + "\n");
        System.out.println("La resta de N - M es: " + (N - M) + "." + "\n");
        System.out.println("La multiplicacion de N * M es: " + (N * M) + "." + "\n");
        System.out.println("La division de N / M es: " + (N / M) + "." + "\n");
        System.out.println("El resto de N % M es: " + (N % M) + "." + "\n");
        
        //X, Y, N y M 
        //operaciones
        System.out.println("La suma entre "+ N + " + "+ X + " es:" + (N + X) + "." + "\n");
        System.out.println("El conciente entre "+ Y + " / "+ M + " es:" + (Y / M) + "." + "\n");
        System.out.println("El resto entre "+ Y + " % "+ M + " es:" + (Y % M) + "." + "\n");
        System.out.println("El doble de las variables" + X + ", " + Y + ", " + N + " y " + M + " es: " + (X*2) + ", " + (Y*2) + ", " + (N*2) + " y " + (M*2) + "." + "\n");
    }
    
}
